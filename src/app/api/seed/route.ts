export async function GET() {
  const supabase = await createClient()

  // Check if the current user is authenticated
  const { data: { user } } = await supabase.auth.getUser()
  if (!user) {
    return NextResponse.json({ error: '로그인이 필요합니다.' }, { status: 401 })
  }

  // Promote user to super_admin for seeding
  await supabase.from('profiles').update({ role: 'super_admin' }).eq('id', user.id)

  const results = []
  for (const article of articles) {
    const { data, error } = await supabase
      .from('articles')
      .insert({ ...article, author_id: user.id })
      .select('id, title')

    results.push({
      title: article.title,
      success: !error,
      error: error?.message ?? null,
      id: data?.[0]?.id ?? null,
    })
  }

  return NextResponse.json({
    message: `${results.filter(r => r.success).length}/${results.length}개 기사 등록 완료`,
    results,
  })
}
